# 1 - Faça um Programa que peça dois números e imprima o maior deles.

num1 = int(input('Informe um número: '))
num2 = int(input('Informe outra número: '))

if num1 > num2:
    print('Este número é maior: ',num1)
else:
    print('Este número é maior: ',num2)